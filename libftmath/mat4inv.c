#include "libftmath.h"

t_mat4	mat4inv(t_mat4 a)
{
	return ((t_mat4){
		(t_vec4){a.x.x, a.y.x, a.z.x, a.w.x},
		(t_vec4){a.x.y, a.y.y, a.z.y, a.w.y},
		(t_vec4){a.x.z, a.y.z, a.z.z, a.w.z},
		(t_vec4){a.x.w, a.y.w, a.z.w, a.w.w}});
}
