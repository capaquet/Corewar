#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t i;

	i = 0;
	while (i < n)
	{
		if (((const unsigned char*)s)[0] == (unsigned char)c)
			return ((void*)s);
		s++;
		i++;
	}
	return (NULL);
}
